package com.example.bloodlife;

public class Profile {
    private String u_name;
    private String u_blood_gr;
    private int u_no_unit;
    private String u_hospital;
    private int u_po_nu;
    private String u_hometown;

    public Profile() {
    }

    public String getU_name() {
        return u_name;
    }

    public void setU_name(String u_name) {
        this.u_name = u_name;
    }

    public String getU_blood_gr() {
        return u_blood_gr;
    }

    public void setU_blood_gr(String u_blood_gr) {
        this.u_blood_gr = u_blood_gr;
    }

    public int getU_no_unit() {
        return u_no_unit;
    }

    public void setU_no_unit(int u_no_unit) {
        this.u_no_unit = u_no_unit;
    }

    public String getU_hospital() {
        return u_hospital;
    }

    public void setU_hospital(String u_hospital) {
        this.u_hospital = u_hospital;
    }

    public int getU_po_nu() {
        return u_po_nu;
    }

    public void setU_po_nu(int u_po_nu) {
        this.u_po_nu = u_po_nu;
    }

    public String getU_hometown() {
        return u_hometown;
    }

    public void setU_hometown(String u_hometown) {
        this.u_hometown = u_hometown;
    }
}

